function sqlEscape(value) {
  return String(value).replace(/'/g, "''");
}

function asRuleRow(frameworkName, rule) {
  const values = [
    `((SELECT id FROM frameworks WHERE name = '${frameworkName}'))`,
    `'${sqlEscape(rule.ruleId)}'`,
    `'${sqlEscape(rule.title)}'`,
    `'${sqlEscape(rule.description)}'`,
    `'${rule.severity}'`,
    `'${rule.category}'`,
    `'${sqlEscape(rule.citation)}'`,
    `'${sqlEscape(rule.remediation)}'`,
    `'${rule.patternType}'`,
    `'${sqlEscape(JSON.stringify(rule.patternConfig))}'`,
    rule.isRequired ? '1' : '0',
  ];
  return `(${values.join(', ')})`;
}

function configRule(
  ruleId,
  title,
  description,
  severity,
  category,
  citation,
  remediation,
  patterns,
  checkFiles = policyCheckFiles,
  isRequired = true,
) {
  return {
    ruleId,
    title,
    description,
    severity,
    category,
    citation,
    remediation,
    patternType: 'config_pattern',
    patternConfig: {
      scope: 'project',
      checkFiles,
      patterns,
    },
    isRequired,
  };
}

